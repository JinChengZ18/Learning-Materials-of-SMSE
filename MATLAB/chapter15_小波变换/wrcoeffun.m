clear all;
clc;
N=80;
t=1:N;
sig1=sin(0.2*t);%生成正弦信号
sig2(1:40)=((1:40)-1)/40;sig2(41:N)=(80-(41:80))/40;%生成三角波信号
x=sig1+sig2;
[c,l]=wavedec(x,2,'db6');%进行两层小波分解
a2=wrcoef('a',c,l,'db6',2);
a1=wrcoef('a',c,l,'db6',1);%重构第1~2层逼近系数
d2=wrcoef('d',c,l,'db6',2);
d1=wrcoef('d',c,l,'db6',1);%重构第1~2层细节系数
subplot(511);
plot(x,'linewidth',2);
ylabel('原始信号');
xlabel('信号序列');
subplot(512);
plot(a2,'linewidth',2);
ylabel('a2');
xlabel('信号序列');
subplot(513);
plot(a1,'linewidth',2);
ylabel('a1');
xlabel('信号序列');
subplot(514);
plot(d2,'linewidth',2);
ylabel('d2');
xlabel('信号序列');
subplot(515);
plot(d1,'linewidth',2);
ylabel('d1');
xlabel('信号序列');